package com.sadullaev.webProject.form.freeRooms;

public class FreeRoomSearchForm {
	
	/**
	 * Instance variables
	 */
	
	private String date;
	private String room;
	private String uhr;
	private String dauer;
	private String number;
	
	/**
	 * Getter function for date
	 * @return date
	 */
	public String getDate() {
		return date;
	}
	
	/**
	 * Setter function for date
	 * @param date
	 */
	public void setDate(String date) {
		this.date = date;
	}
	
	/**
	 * Getter function for room
	 * @return room
	 */
	public String getRoom() {
		return room;
	}
	
	/**
	 * Setter function for room
	 * @param room
	 */
	public void setRoom(String room) {
		this.room = room;
	}
	
	/**
	 * Getter function for clock
	 * @return clock
	 */
	public String getUhr() {
		return uhr;
	}
	
	/**
	 * Setter function for clock
	 * @param uhr
	 */
	public void setUhr(String uhr) {
		this.uhr = uhr;
	}
	
	/**
	 * Getter function time
	 * @return time
	 */
	public String getDauer() {
		return dauer;
	}
	
	/**
	 * Setter function for time
	 * @param dauer
	 */
	public void setDauer(String dauer) {
		this.dauer = dauer;
	}
	
	/**
	 * Getter function for number
	 * @return
	 */
	public String getNumber() {
		return number;
	}
	
	/**
	 * Setter function for number
	 * @param number
	 */
	public void setNumber(String number) {
		this.number = number;
	}

}
