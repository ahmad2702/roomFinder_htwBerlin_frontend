package com.sadullaev.webProject.form.events;

public class EventSearchForm {
	
	/**
	 * Instance variables
	 */
	
	private String title;
	private String date;
	private String lecturer;
	private String number;
	
	/**
	 * Getter function for title
	 * @return title
	 */
	public String getTitle() {
		return title;
	}
	
	/**
	 * Setter function for title
	 * @param title
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	
	/**
	 * Getter function for date
	 * @return date
	 */
	public String getDate() {
		return date;
	}
	
	/**
	 * Setter function for date
	 * @param date
	 */
	public void setDate(String date) {
		this.date = date;
	}
	
	/**
	 * Getter function for lecturer
	 * @return lecturer
	 */
	public String getLecturer() {
		return lecturer;
	}
	
	/**
	 * Setter function for lecturer
	 * @param lecturer
	 */
	public void setLecturer(String lecturer) {
		this.lecturer = lecturer;
	}
	
	/**
	 * Getter function for number
	 * @return number
	 */
	public String getNumber() {
		return number;
	}
	
	/**
	 * Setter function for number
	 * @param number
	 */
	public void setNumber(String number) {
		this.number = number;
	}

}
